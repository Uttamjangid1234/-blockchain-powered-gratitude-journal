let web3;
let contract;

window.addEventListener("load", async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: "eth_requestAccounts" });
        contract = new web3.eth.Contract(abi, contractAddress);
        loadNFTs();
    } else {
        alert("Please install MetaMask to use this DApp.");
    }
});

async function createNFT() {
    const name = document.getElementById("nftName").value;
    const metadata = document.getElementById("nftMetadata").value;
    const accounts = await web3.eth.getAccounts();
    await contract.methods.createNFT(name, metadata).send({ from: accounts[0] });
    loadNFTs();
}

async function loadNFTs() {
    const nftList = document.getElementById("nftList");
    nftList.innerHTML = "";
    const totalNFTs = await contract.methods.nextId().call();
    for (let i = 0; i < totalNFTs; i++) {
        const nft = await contract.methods.nfts(i).call();
        const nftItem = document.createElement("div");
        nftItem.innerHTML = `<h3>${nft.name}</h3><p>Owner: ${nft.owner}</p>`;
        nftList.appendChild(nftItem);
    }
}
